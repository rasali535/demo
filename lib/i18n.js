export const I18nContext = createContext({ lang: 'en', t: (k) => k, setLang: () => {} });

export function I18nProvider({ children }) {
  const [lang, setLang] = useState('en');
  useEffect(() => {
    try {
      const saved = localStorage.getItem('magri_lang');
      if (saved && dict[saved]) setLang(saved);
    } catch {}
  }, []);
  const setLangPersist = (l) => {
    setLang(l);
    try { localStorage.setItem('magri_lang', l); } catch {}
  };
  const t = (key, vars) => {
    let s = dict[lang]?.[key] ?? dict.en?.[key] ?? key;
    if (vars) for (const k of Object.keys(vars)) s = s.replaceAll('{' + k + '}', vars[k]);
    return s;
  };
  return (
    <I18nContext.Provider value={{ lang, t, setLang: setLangPersist }}>{children}</I18nContext.Provider>
  );
}

export function useT() {
  return useContext(I18nContext);
}
